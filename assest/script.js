// --- FONT RESIZER ---
let currentSize = 16;
function changeFontSize(n) {
    if (n === 0) currentSize = 16;
    else currentSize += n;
    
    // Limits
    if(currentSize > 24) currentSize = 24;
    if(currentSize < 12) currentSize = 12;

    document.documentElement.style.setProperty('--base-size', currentSize + 'px');
}

// --- DARK MODE ---
